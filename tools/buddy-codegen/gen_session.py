def main():
    parser = argparse.ArgumentParser(
        description="Generate C++ ModelSession .h/.cpp from a full model config."
    )
    parser.add_argument(
        "--config", required=True, help="Path to full config JSON"
    )
    parser.add_argument(
        "--output-dir",
        required=True,
        help=(
            "Output root: writes buddy/runtime/models/ModelSession.h and "
            "ModelSession.cpp (no other copies; CMake uses build dir only)."
        ),
    )
    args = parser.parse_args()

    with open(args.config) as f:
        config = json.load(f)

    os.makedirs(args.output_dir, exist_ok=True)
    h_dir = os.path.join(args.output_dir, "buddy", "runtime", "models")
    os.makedirs(h_dir, exist_ok=True)

    h_path = os.path.join(h_dir, "ModelSession.h")
    cpp_path = os.path.join(args.output_dir, "ModelSession.cpp")

    with open(h_path, "w") as f:
        f.write(gen_header(config))
    print(f"[gen_session] Written: {h_path}", file=sys.stderr)

    with open(cpp_path, "w") as f:
        f.write(gen_impl(config))
    print(f"[gen_session] Written: {cpp_path}", file=sys.stderr)


if __name__ == "__main__":
    main()
