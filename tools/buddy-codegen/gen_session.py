def _macro_prefix(config: dict) -> str:
    """Return the C macro prefix, e.g. BUDDY_DSR1."""
    family = config["model_family"].upper()
    short = {"DEEPSEEK_R1": "DSR1"}.get(family, family)
    return f"BUDDY_{short}"


