def gen_impl(config: dict) -> str:
    out = StringIO()

    def _p(*a, **kw):
        print(*a, file=out, **kw)

    p = _p

    mp = _macro_prefix(config)
    shape = config["shape"]
    weights = config["weights"]
    kv_cpp = config["cpp_types"]["kv"]
    logits_cpp = config["cpp_types"]["logits"]
    kv_layers = shape["kv_layers"]
    kv_memref = config["cpp_types"]["kv_memref"]
    logits_memref = config["cpp_types"]["logits_memref"]
    need_logits_conv = logits_cpp != "float"

    p(_CPP_FILE_PROLOGUE)
    p('#include "buddy/runtime/models/ModelSession.h"')
    p('#include "buddy/runtime/llm/KVCacheManager.h"')
    p()
    p("#include <cstring>")
    p("#include <dlfcn.h>")
    p("#include <fstream>")
    p("#include <stdexcept>")
    p("#include <string>")
    p()
    p(
        "// std::launder is in <new> (C++17). Use it when accessing a MemRef object"
    )
    p("// through a char* pointer obtained by reinterpret_cast.")
    p("#include <new> // for std::launder, placement new")
    p()
    p("namespace buddy {")
    p("namespace runtime {")
    p()

    # ── Half-to-float helper (emitted only when needed) ──────────────────────
    if need_logits_conv:
        p("namespace {")
        p("inline float halfToFloat(uint16_t h) {")
        p("  uint32_t sign = (uint32_t)(h >> 15) << 31;")
        p("  uint32_t exp = (h >> 10) & 0x1f;")
        p("  uint32_t mant = h & 0x3ff;")
        p("  uint32_t f;")
        p("  if (exp == 0)")
        p(
            "    f = (mant == 0) ? sign : sign | ((127 - 14) << 23) | (mant << 13);"
        )
        p("  else if (exp == 31)")
        p("    f = sign | 0x7f800000u | (mant << 13);")
        p("  else")
        p("    f = sign | ((exp + 127 - 15) << 23) | (mant << 13);")
        p("  float result;")
        p("  std::memcpy(&result, &f, 4);")
        p("  return result;")
        p("}")
        p("} // namespace")
        p()

    # ── KVCacheABI ───────────────────────────────────────────────────────────
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// KVCacheABI — contiguous memory block for the MLIR result struct")
    p("//")
    p(
        "// MLIR-compiled _mlir_ciface_* functions write their outputs into a struct"
    )
    p("// whose layout is:")
    p(
        f"//   [kv0..kv{kv_layers - 1} : {kv_memref} x {kv_layers}][logits : {logits_memref}]"
    )
    p("//")
    p(
        "// Because MemRef<T,N>() is protected, we store these objects in char arrays"
    )
    p("// and construct/destruct them explicitly via placement new.")
    p(
        "// The pointer arithmetic uses sizeof(MemRef<...>) for correct alignment (alignas)."
    )
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()
    p("struct KVCacheABI {")
    p(f"  alignas({kv_memref}) char kv_[sizeof({kv_memref}) *")
    p(f"                                     {mp}_KV_LAYERS];")
    p(f"  alignas({logits_memref}) char logits_[sizeof({logits_memref})];")
    p()
    p(f"  {kv_memref} &kv(int i) {{")
    p(f"    return *std::launder(reinterpret_cast<{kv_memref} *>(")
    p(f"        kv_ + i * sizeof({kv_memref})));")
    p("  }")
    p(f"  {logits_memref} &logits() {{")
    p(
        f"    return *std::launder(reinterpret_cast<{logits_memref} *>(logits_));"
    )
    p("  }")
    p("};")
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// Function pointer types (private to this TU)")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()

    # ── Function pointer types ───────────────────────────────────────────────
    weight_ptr_types = ", ".join(
        f"MemRef<{w['cpp_type']}, 1> *" for w in weights
    )

    p(
        f"using PrefillFn = void (*)(KVCacheABI *, {weight_ptr_types}, Text<size_t, 2> *);"
    )
    p()

    kv4 = f"{kv_memref} *"
    p(f"using KV4 = {kv4};")

    kv_args_per_line = 12
    kv_lines = []
    for start in range(0, kv_layers, kv_args_per_line):
        end = min(start + kv_args_per_line, kv_layers)
        kv_lines.append(", ".join(["KV4"] * (end - start)))

    p(f"using DecodeFn = void (*)(KVCacheABI *, {weight_ptr_types},")
    p(
        "                          MemRef<long long, 2> *, MemRef<long long, 1> *,"
    )
    for i, line in enumerate(kv_lines):
        comma = "," if i < len(kv_lines) - 1 else ");"
        p(f"                          {line}{comma}")
    p()

    # ── Impl struct ──────────────────────────────────────────────────────────
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// ModelSession::Impl")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()
    p("struct ModelSession::Impl {")
    p("  KVCacheABI abi;  // raw storage for kv + logits")
    p(f"  {kv_memref} *kvPtrs[{mp}_KV_LAYERS];  // host-side pointer array")
    p("  bool abiInitialized = false;  // placement-new has been called")
    p("  void *soHandle = nullptr;")
    p("  std::vector<void *> depSoHandles;")
    p("  PrefillFn prefillFn = nullptr;")
    p("  DecodeFn decodeFn = nullptr;")
    p()
    p("  ~Impl() {")
    p(
        "    // Explicitly destroy placement-new'd objects before releasing the .so."
    )
    p("    if (abiInitialized) {")
    p(f"      for (int i = 0; i < {mp}_KV_LAYERS; ++i)")
    p(f"        abi.kv(i).~{kv_memref}();")
    p(f"      abi.logits().~{logits_memref}();")
    p("    }")
    p("    if (soHandle) {")
    p("      dlclose(soHandle);")
    p("      soHandle = nullptr;")
    p("    }")
    p(
        "    for (auto it = depSoHandles.rbegin(); it != depSoHandles.rend(); ++it) {"
    )
    p("      if (*it)")
    p("        dlclose(*it);")
    p("    }")
    p("    depSoHandles.clear();")
    p("  }")
    p()
    p(
        "  /// Preload dependent shared libs, then dlopen model and resolve symbols."
    )
    p("  void loadSo(const std::string &soPath,")
    p("              const std::vector<std::string> &dependentSoPaths) {")
    p("    for (const auto &depPath : dependentSoPaths) {")
    p(
        "      void *depHandle = dlopen(depPath.c_str(), RTLD_NOW | RTLD_GLOBAL);"
    )
    p("      if (!depHandle)")
    p(
        '        throw std::runtime_error("[BuddyRuntime] dependent dlopen failed: " + depPath +'
    )
    p('                                 "\\n  " + dlerror());')
    p("      depSoHandles.push_back(depHandle);")
    p("    }")
    p()
    p("    soHandle = dlopen(soPath.c_str(), RTLD_NOW | RTLD_LOCAL);")
    p("    if (!soHandle)")
    p(
        '      throw std::runtime_error("[BuddyRuntime] dlopen failed: " + soPath +'
    )
    p('                               "\\n  " + dlerror());')
    p()
    p("    prefillFn = reinterpret_cast<PrefillFn>(")
    p('        dlsym(soHandle, "_mlir_ciface_forward_prefill"));')
    p("    if (!prefillFn)")
    p("      throw std::runtime_error(")
    p(
        '          "[BuddyRuntime] symbol not found: _mlir_ciface_forward_prefill\\n  " +'
    )
    p("          std::string(dlerror()));")
    p()
    p("    decodeFn = reinterpret_cast<DecodeFn>(")
    p('        dlsym(soHandle, "_mlir_ciface_forward_decode"));')
    p("    if (!decodeFn)")
    p("      throw std::runtime_error(")
    p(
        '          "[BuddyRuntime] symbol not found: _mlir_ciface_forward_decode\\n  " +'
    )
    p("          std::string(dlerror()));")
    p("  }")
    p("};")
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// ModelSession lifecycle")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()

    # ── Lifecycle ────────────────────────────────────────────────────────────
    p("ModelSession::ModelSession(const Config &cfg) : cfg_(cfg) {")
    p("  if (cfg_.modelSoPath.empty())")
    p("    throw std::runtime_error(")
    p('        "[BuddyRuntime] Config.modelSoPath must not be empty.");')
    p("  allocateKVCache();")
    p("  impl_->loadSo(cfg_.modelSoPath, cfg_.dependentSoPaths);")
    p("}")
    p()
    p("ModelSession::~ModelSession() = default;")
    p()
    p("std::unique_ptr<ModelSession> ModelSession::create(const Config &cfg) {")
    p("  return std::unique_ptr<ModelSession>(new ModelSession(cfg));")
    p("}")
    p()

    # ── allocateKVCache ──────────────────────────────────────────────────────
    kv_sizeof = f"sizeof({kv_cpp})"

    p("void ModelSession::allocateKVCache() {")
    p("  impl_ = std::make_unique<Impl>();")
    p()
    p("  const uint64_t elemsPerLayer =")
    p("      (uint64_t)cfg_.headNum * cfg_.maxTokenLen * cfg_.hiddenSize;")
    p(f"  const uint64_t bytesPerLayer = elemsPerLayer * {kv_sizeof};")
    p()
    p("  // --- KV cache: one MemRef per layer, constructed in-place ---")
    p(
        "  intptr_t kvShape[4] = {1, cfg_.headNum, cfg_.maxTokenLen, cfg_.hiddenSize};"
    )
    p()
    p("  for (int i = 0; i < cfg_.kvLayers; ++i) {")
    p("    BufferDesc desc;")
    p('    desc.name = "kv" + std::to_string(i);')
    p("    desc.role = BufferRole::State;")
    p("    desc.lifetime = BufferLifetime::Session;")
    p("    desc.bytes = bytesPerLayer;")
    p("    desc.id = (uint32_t)(100 + i);")
    p()
    p("    auto &bv = pool_.allocate(desc);")
    p(
        "    // Placement-new: construct a MemRef view into the BufferPool buffer."
    )
    p("    new (&impl_->abi.kv(i))")
    p(f"        {kv_memref}(reinterpret_cast<{kv_cpp} *>(bv.data), kvShape);")
    p("    impl_->kvPtrs[i] = &impl_->abi.kv(i);")
    p("  }")
    p()
    p("  // --- Logits: prefill shape {1, maxTokenLen, vocabSize} ---")
    p("  const uint64_t logitsBytes =")
    p(f"      (uint64_t)cfg_.maxTokenLen * cfg_.vocabSize * {kv_sizeof};")
    p("  {")
    p("    BufferDesc desc;")
    p('    desc.name = "logits";')
    p("    desc.role = BufferRole::Output;")
    p("    desc.lifetime = BufferLifetime::Session;")
    p("    desc.bytes = logitsBytes;")
    p("    desc.id = 200;")
    p()
    p("    auto &bv = pool_.allocate(desc);")
    p("    intptr_t lshape[3] = {1, cfg_.maxTokenLen, cfg_.vocabSize};")
    p("    new (&impl_->abi.logits())")
    p(
        f"        {logits_memref}(reinterpret_cast<{logits_cpp} *>(bv.data), lshape);"
    )
    p("  }")
    p()
    p("  impl_->abiInitialized = true;")
    p()
    p("  // --- Decode step inputs (small, reused every call) ---")
    p("  {")
    p("    intptr_t tshape[2] = {1, 1};")
    p("    decodeTokenInput_ = std::make_unique<MemRef<long long, 2>>(tshape);")
    p("    intptr_t pshape[1] = {1};")
    p("    cachePosition_ = std::make_unique<MemRef<long long, 1>>(pshape);")
    p("  }")
    p("}")
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// loadWeights")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()

    # ── loadWeights ──────────────────────────────────────────────────────────
    p("void ModelSession::loadWeights(const std::vector<std::string> &paths) {")
    p(f"  if (paths.size() < {len(weights)}u)")
    p(
        f'    throw std::runtime_error("[BuddyRuntime] Expected {len(weights)} weight '
        f'file(s), got " + std::to_string(paths.size()));'
    )
    p()
    for idx, w in enumerate(weights):
        tag = w["tag"]
        cpp_type = w["cpp_type"]
        macro_suffix = (
            "PARAMS_SIZE" if len(weights) == 1 else f"PARAMS_SIZE_{tag.upper()}"
        )
        p("  {")
        p(f"    intptr_t shape[1] = {{{mp}_{macro_suffix}}};")
        p(f"    {tag}_ = std::make_unique<MemRef<{cpp_type}, 1>>(shape);")
        p(f"    std::ifstream f(paths[{idx}], std::ios::binary);")
        p("    if (!f)")
        p(
            f'      throw std::runtime_error("[BuddyRuntime] Cannot open weights: " + '
            f"paths[{idx}]);"
        )
        p(f"    f.read(reinterpret_cast<char *>({tag}_->getData()),")
        p(f"           sizeof({cpp_type}) * {tag}_->getSize());")
        p("    if (f.fail())")
        p(
            f'      throw std::runtime_error("[BuddyRuntime] Read failed: " + '
            f"paths[{idx}]);"
        )
        p("  }")
    p("}")
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// Prefill")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()

    # ── prefill ──────────────────────────────────────────────────────────────
    weight_addrs_internal = ", ".join(f"{w['tag']}_.get()" for w in weights)

    p("void ModelSession::prefill(Text<size_t, 2> &tokens) {")
    p(f"  impl_->prefillFn(&impl_->abi, {weight_addrs_internal}, &tokens);")
    p("  int tokenCount = (int)tokens.getTokenCnt();")
    p("  position_ = tokenCount;")
    p("}")
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// Decode")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()

    # ── decode ───────────────────────────────────────────────────────────────
    p("void ModelSession::decode(int tokenId) {")
    p("  decodeTokenInput_->getData()[0] = (long long)tokenId;")
    p("  cachePosition_->getData()[0] = (long long)position_;")
    p()
    p("  auto &a = impl_->abi;")

    call_parts = ["&a"]
    for w in weights:
        call_parts.append(f"{w['tag']}_.get()")
    call_parts.append("decodeTokenInput_.get()")
    call_parts.append("cachePosition_.get()")
    for i in range(kv_layers):
        call_parts.append(f"&a.kv({i})")

    p("  impl_->decodeFn(")
    line = "      "
    for idx, part in enumerate(call_parts):
        sep = ", " if idx < len(call_parts) - 1 else ");"
        candidate = line + part + sep
        if len(candidate) > 80 and line.strip():
            p(line.rstrip(", ") + ",")
            line = "      " + part + sep
        else:
            line = candidate
    if line.strip():
        p(line)

    p()
    p("  position_ += 1;")
    p("}")
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// Misc")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p()

    # ── Misc methods ─────────────────────────────────────────────────────────
    p("void ModelSession::resetPosition() { position_ = 0; }")
    p()

    p(
        "bool ModelSession::handleKVCacheOverflow(int keepTokenNum, float ropeTheta) {"
    )
    p("  if (position_ < cfg_.maxTokenLen)")
    p("    return false;")
    p()
    p("  const int currentTokens = std::min(position_, cfg_.maxTokenLen);")
    p("  keepTokenNum = std::clamp(keepTokenNum, 0, currentTokens - 1);")
    p(
        "  const int discardLen = std::max(1, (currentTokens - keepTokenNum) / 2);"
    )
    p()
    p("  // Extract raw pointers from KV cache MemRefs for kvcache helpers.")
    p(f"  {kv_cpp} *rawPtrs[{mp}_KV_LAYERS];")
    p("  for (int i = 0; i < cfg_.kvLayers; ++i)")
    p("    rawPtrs[i] = impl_->abi.kv(i).getData();")
    p()
    p("  // Step 1: Discard tokens (memmove + memset).")
    p("  buddy::kvcache::discardKVCache(rawPtrs, cfg_.kvLayers, cfg_.headNum,")
    p("                                 cfg_.maxTokenLen, cfg_.hiddenSize,")
    p(
        "                                 keepTokenNum, discardLen, currentTokens);"
    )
    p()
    p(
        "  // Step 2: Adjust RoPE on surviving tail (now relocated to keepTokenNum)."
    )
    p("  auto inverseFreqs =")
    p(
        "      buddy::kvcache::buildInverseRopeFreqs(ropeTheta, cfg_.hiddenSize);"
    )
    p("  buddy::kvcache::adjustKeyCacheRope(")
    p(
        "      rawPtrs, cfg_.kvLayers, cfg_.headNum, cfg_.maxTokenLen, cfg_.hiddenSize,"
    )
    p("      keepTokenNum, discardLen, currentTokens, inverseFreqs);")
    p()
    p("  // Step 3: Update position.")
    p(
        "  position_ = std::clamp(currentTokens - discardLen, 0, cfg_.maxTokenLen);"
    )
    p("  return true;")

    p("}")
    p()

    # ── logitsData ───────────────────────────────────────────────────────────
    if need_logits_conv:
        p("const float *ModelSession::logitsData(int tokenOffset) const {")
        p("  const int n = cfg_.vocabSize;")
        p("  if ((int)logitsFloat_.size() != n)")
        p("    logitsFloat_.resize(n);")
        p(f"  const {logits_cpp} *raw =")
        p("      impl_->abi.logits().getData() + (size_t)tokenOffset * n;")
        p("  for (int i = 0; i < n; ++i)")
        p("    logitsFloat_[i] = halfToFloat(raw[i]);")
        p("  return logitsFloat_.data();")
        p("}")
    else:
        p("const float *ModelSession::logitsData(int tokenOffset) const {")
        p(
            "  return impl_->abi.logits().getData() + (size_t)tokenOffset * cfg_.vocabSize;"
        )
        p("}")
    p()
    p(
        "std::string ModelSession::loadedSoPath() const { return cfg_.modelSoPath; }"
    )
    p()
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// createFromRax: load config from a packed .rax manifest")
    p(
        "//===----------------------------------------------------------------------===//"
    )
    p("// Shape constants remain at compile-time defaults from macros;")
    p(
        "// createFromRax() sets cfg.modelSoPath and cfg.dependentSoPaths from manifest."
    )
    p()

    # ── createFromRax ────────────────────────────────────────────────────────
    p("std::unique_ptr<ModelSession>")
    p("ModelSession::createFromRax(const std::string &raxPath,")
    p("                            ModelManifest &resolvedManifest) {")
    p("  resolvedManifest = ModelManifest::loadFromRax(raxPath);")
    p("  Config cfg;")
    p("  cfg.modelSoPath = resolvedManifest.soPath;")
    p("  cfg.dependentSoPaths = resolvedManifest.dependentSoPaths;")
    p("  return create(cfg);")
    p("}")
    p()
    p("} // namespace runtime")
    p("} // namespace buddy")
    p()

    return out.getvalue()


