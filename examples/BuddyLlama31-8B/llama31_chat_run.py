# ===- deepseek_chat_run.py --------------------------------------------------
#
# Interactive DeepSeek-R1-Distill-Qwen-1.5B chat runner driving P150A via
# ``ttrt.runtime``.  Mirrors ``buddy-deepseek-r1-main.cpp`` (the f32 CPU
# reference) in flow and console output so results from the two backends are
# directly comparable.
#
# Prerequisites (see ``run_deepseek_p150_chat.sh``):
#   * ``deepseek_prefill_static.ttnn`` and ``deepseek_decode_static.ttnn`` built
#     from ``buddy-deepseek-r1-lower-ttir.py --static-cache --max-cache-len N``.
#   * ``chat_artifacts/{prefill,decode}/*`` generated by
#     ``deepseek_chat_prepare.py`` with a matching ``--max-cache-len``.
#
# ===---------------------------------------------------------------------------

